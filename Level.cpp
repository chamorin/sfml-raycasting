void Level::draw()
{
    for (auto &vec : m_vecBox)
    {
        m_renderWindow.draw(vec);
    }
}

bool Level::checkLevelCase(unsigned int dx, unsigned int dy)
{
    if (m_vecLevel[dy][dx])
        return true;
    else
        return false;
}